import { ApiHideProperty, ApiProperty, ApiPropertyOptional } from '@nestjs/swagger';
import { Type } from 'class-transformer';
import {
  IsArray,
  IsBoolean,
  IsInt,
  IsOptional,
  IsString,
  IsUUID,
  ValidateNested,
} from 'class-validator';

export class JobSkillInputDto {
  @ApiPropertyOptional({ description: 'Ordering index in job_skill', example: 0 })
  @IsOptional()
  @IsInt()
  @Type(() => Number)
  index?: number;

  @ApiProperty({
    description: 'Skill id stored in job_skill.skill_id (use Neo4j skill element id/eid)',
    example: '4:abc123:0',
  })
  @IsString()
  skill_id: string;

  @ApiPropertyOptional({ description: 'Skill display name', example: 'TypeScript' })
  @IsOptional()
  @IsString()
  skill_name?: string;
}

